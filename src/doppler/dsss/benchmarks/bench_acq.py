"""Benchmark for BurstAcquisition — the acquisition unit of work (one CAF tile).

One ``push`` of ``N = doppler_bins*code_bins`` samples is the engine's smallest
reusable unit: ring write → slow-time Doppler FFT → single-row corr2d → CFAR.
These benchmarks measure its latency and derive per-core sample throughput,
which feeds the compute model in ``docs/design/dsss-acquisition.md``.

The grids pin ``doppler_bins == reps`` with a deliberately low sizing
``cn0_dbhz`` (which the engine reports as ``underpowered`` — irrelevant for a
throughput probe, so the warning is filtered).

``BurstAcquisition.push`` releases the GIL, so the same kernel scales
thread-per-shard across cores.

Run: pytest src/doppler/dsss/benchmarks/bench_acq.py --benchmark-only
"""

import warnings

import numpy as np

from doppler.dsss import BurstAcquisition
from doppler.wfm import PN, mls_poly


def _code(sf, length):
    return PN(poly=mls_poly(length), seed=1, length=length).generate(sf)


def _acq(sf, length, *, reps, spc):
    """Force doppler_bins == reps via a weak sizing C/N0 (warning filtered)."""
    with warnings.catch_warnings():
        warnings.simplefilter("ignore", UserWarning)
        return BurstAcquisition(
            _code(sf, length),
            reps=reps,
            spc=spc,
            chip_rate=1.0e6,
            cn0_dbhz=20.0,
            pfa=1e-3,
            pd=0.9,
        )


def _tile(a, benchmark):
    """Benchmark one CAF tile (push of N samples) and record throughput."""
    n = a.doppler_bins * a.code_bins
    rng = np.random.default_rng(0)
    x = (rng.standard_normal(n) + 1j * rng.standard_normal(n)).astype(
        np.complex64
    )
    benchmark(a.push, x)
    if benchmark.stats:
        benchmark.extra_info["N"] = n
        benchmark.extra_info["MSa_s"] = n / benchmark.stats["mean"] / 1e6


def test_bench_tile_test_grid(benchmark):
    """Test-suite grid: sf=31, spc=4, reps=16 → N=1984 (code_bins=124)."""
    a = _acq(31, 5, reps=16, spc=4)
    _tile(a, benchmark)


def test_bench_tile_worked(benchmark):
    """Worked-case grid: GPS-class length-1023 code, spc=2, reps=10 → N≈20k.

    Measured (12-core x86, this machine, regenerated by ``make gallery``):
    ~22-29 MSa/s per core for the tile; thread-per-shard scaling ~3.4x on 8,
    ~4.9x on 12 (memory-bandwidth bound past a few cores, like ddc_fn).
    """
    a = _acq(1023, 10, reps=10, spc=2)
    _tile(a, benchmark)
