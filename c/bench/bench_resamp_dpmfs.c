/* Build flat c0/c1 arrays from per-m sub-arrays                     */
/* ------------------------------------------------------------------ */

static void
build_flat_m1 (const float *m0, const float *m1, size_t N, float *out)
{
  memcpy (&out[0 * N], m0, N * sizeof (float));
  memcpy (&out[1 * N], m1, N * sizeof (float));
}

static void
build_flat_m2 (const float *m0, const float *m1, const float *m2, size_t N,
               float *out)
{
  memcpy (&out[0 * N], m0, N * sizeof (float));
  memcpy (&out[1 * N], m1, N * sizeof (float));
  memcpy (&out[2 * N], m2, N * sizeof (float));
}

static void
build_flat (const float *m0, const float *m1, const float *m2, const float *m3,
            size_t N, float *out)
{
  memcpy (&out[0 * N], m0, N * sizeof (float));
  memcpy (&out[1 * N], m1, N * sizeof (float));
  memcpy (&out[2 * N], m2, N * sizeof (float));
  memcpy (&out[3 * N], m3, N * sizeof (float));
}

/* ------------------------------------------------------------------ */
/* Benchmark runner                                                   */
/* ------------------------------------------------------------------ */

static double
bench_rate_dpmfs (const float *c0, const float *c1, size_t M, size_t N,
                  double rate, const dp_cf32_t *input, dp_cf32_t *output,
                  size_t max_out)
{
  dp_resamp_dpmfs_t *r = dp_resamp_dpmfs_create (M, N, c0, c1, rate);

  dp_resamp_dpmfs_execute (r, input, BLOCK_SIZE, output, max_out);
  dp_resamp_dpmfs_reset (r);

  struct timespec t0, t1;
  clock_gettime (CLOCK_MONOTONIC, &t0);
  for (int i = 0; i < ITERATIONS; i++)
    dp_resamp_dpmfs_execute (r, input, BLOCK_SIZE, output, max_out);
  clock_gettime (CLOCK_MONOTONIC, &t1);

  dp_resamp_dpmfs_destroy (r);

  double sec = elapsed_sec (&t0, &t1);
  return (double)ITERATIONS * BLOCK_SIZE / sec / 1e6;
}

